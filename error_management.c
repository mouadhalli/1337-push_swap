#include "push_swap.h"

void    filter_input(char **argv)
{
    int		i;
    long	nbr;

    i = 1;
    nbr = 0;
    while (argv[i])
    {
        if (!is_nbr(argv[i]))
            exit_error(2);
        i++;
    }
}
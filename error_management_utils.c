#include "push_swap.h"

int		is_nbr(char *str)
{
	int		i;

	i = 0;
    if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}